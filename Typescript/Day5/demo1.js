// 4 basic types
//1 function without argument & without Return
//2 function with argument & without Return
//3 function without argument & with Return
//4 function with argument & with Return
function add1() {
    return (3 + 5);
}
console.log("Addition is " + add1());
function add2() {
    return (5 + 5);
}
console.log("Addition is " + add2());
function add3(a, b) {
    return (a + b);
}
console.log("Addition is " + add3(6, 6));
